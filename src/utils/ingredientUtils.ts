// 食材の詳細情報を取得する関数
export function getIngredientDetails(ingredient: Ingredient): FoodItem | null {
  // マッピングから正規化された食材名を取得
  const normalizedName = INGREDIENT_MAPPING[ingredient.name] || ingredient.name;
  
  // データベースから食材情報を取得
  const foodInfo = FOOD_DATABASE.find(food => food.name === normalizedName);
  return foodInfo || null;
}

// 食材名を正規化する関数
export function normalizeIngredientName(name: string): string {
  return INGREDIENT_MAPPING[name] || name;
}